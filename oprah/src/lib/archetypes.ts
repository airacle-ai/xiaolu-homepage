import type { DimensionResult, IdentityLabel } from './supabase'

export interface Archetype {
  id: string
  name: string
  tagline: string
  description: string
  dimensions: DimensionResult
  identity_label: IdentityLabel
  category: 'relationship' | 'thinking' | 'energy' | 'growth'
}

function dim(result: string, confidence: number, evidence: string, insight: string): DimensionResult['thinking_styles']['info_processing'] {
  return { result, confidence, evidence, insight }
}

function val(result: number, confidence: number, evidence: string, insight: string): DimensionResult['values']['truth_vs_kindness'] {
  return { result, confidence, evidence, insight }
}

function att(result: string, anxiety: number, avoidance: number, confidence: number, evidence: string, insight: string): DimensionResult['relationship_patterns']['attachment_style'] {
  return { result, anxiety_score: anxiety, avoidance_score: avoidance, confidence, evidence, insight }
}

function intim(primary: string, secondary: string, confidence: number, evidence: string, insight: string): DimensionResult['relationship_patterns']['intimacy_language'] {
  return { primary, secondary, confidence, evidence, insight }
}

function unf(description: string, confidence: number, evidence: string, insight: string): DimensionResult['unfinished_self']['suppressed_expression'] {
  return { description, confidence, evidence, insight }
}

